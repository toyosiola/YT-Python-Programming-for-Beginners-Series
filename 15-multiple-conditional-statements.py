number = int(input("Enter a number: "))

if number > 0:
  print("Your provided number is positive")
elif number == 0:
  print("Your provided number is 0")
else:
    print("Your provided number is negative")


if number % 2 == 0:
  print("Your provided number is even number")
else:
    print("Your provided number is odd number")

if number % 5 == 0:
  print("Your provided number is a multiple of 5")
